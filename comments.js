function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function tierOf(total) {
  if (total >= 15) return 2;
  if (total >= 9) return 1;
  return 0;
}

// 누적 total 이 3의 배수를 새로 넘었는지 판정
export function crossedThreshold(oldTotal, newTotal) {
  return newTotal > oldTotal && Math.floor(newTotal / 3) > Math.floor(oldTotal / 3);
}

// 3개비 단위 단계(step)에 따라 라플란드/켈시 번갈아, 흡연량에 따라 톤 분기
export function pickComment(newTotal) {
  const step = Math.floor(newTotal / 3); // 3→1, 6→2, 9→3 ...
  const char = step % 2 === 1 ? lappland : kaltsit;
  const line = pick(char.tiers[tierOf(newTotal)]);
  return { who: char.who, emoji: char.emoji, line };
}
